var isCustomAttribute = RegExp.prototype.test.bind(
    new RegExp('^(data|aria)-[:A-Z_a-z\\u00C0-\\u00D6\\u00D8-\\u00F6\\u00F8-\\u02FF\\u0370-\\u037D\\u037F-\\u1FFF\\u200C-\\u200D\\u2070-\\u218F\\u2C00-\\u2FEF\\u3001-\\uD7FF\\uF900-\\uFDCF\\uFDF0-\\uFFFD\\-.0-9\\u00B7\\u0300-\\u036F\\u203F-\\u2040]*$')
);

function getHTMLAttributeName(name) {
    var htmlPropertyName = null;
    if (Properties[name] !== undefined) {
        htmlPropertyName = HTMLProperties[name] || name;
    }
    else if (isCustomAttribute(name)) {
        htmlPropertyName = name;
    }
    return htmlPropertyName;
}

function attributeHasBooleanValue(name) {
    return Properties[name] === HAS_BOOLEAN_VALUE;
}

module.exports = {
    getHTMLAttributeName: getHTMLAttributeName,
    attributeHasBooleanValue: attributeHasBooleanValue
};
